import { useState } from 'react';
import Icon from '../common/Icon';
import { registerUser } from '../../services/api';

const RegisterModal = ({ isOpen, onClose, onRegisterSuccess }) => {
const [email, setEmail] = useState('');
const [password, setPassword] = useState('');
const [firstName, setFirstName] = useState('');
const [lastName, setLastName] = useState('');
const [role, setRole] = useState('buyer');
const [showPassword, setShowPassword] = useState(false);
const [error, setError] = useState('');
const [loading, setLoading] = useState(false);

if (!isOpen) return null;

const handleGoogleLogin = () => {
// Redirect to Google OAuth
window.location.href = '/api/v1/auth/google';
};

const handleSubmit = async (e) => {
e.preventDefault();
setError('');
setLoading(true);

try {
    const response = await registerUser(email, password, firstName, lastName, role);
    
    // Save user data to localStorage
    localStorage.setItem('user', JSON.stringify(response.data));
    localStorage.setItem('isLoggedIn', 'true');
    
    console.log('Register successful', response.data);
    
    // Close modal first
    onClose();
    
    // Call onRegisterSuccess callback to update header
    if (onRegisterSuccess) {
    onRegisterSuccess(response.data);
    }
    
    setEmail('');
    setPassword('');
    setFirstName('');
    setLastName('');
    setRole('buyer');
    setTimeout(() => window.location.reload(), 100);
} catch (err) {
    console.log(setEmail, setPassword, setFirstName, setLastName, setRole);
    console.error('Register failed:', err);
    setError('Incorrect email or password');
} finally {
    setLoading(false);
}
};

return (
<>
    <div 
    className="fixed inset-0 bg-black/50 z-[9998]"
    onClick={onClose}
    />
    <div className="fixed top-1/2 left-1/2 transform -translate-x-1/2 -translate-y-1/2 bg-[#4618ac] p-8 w-[400px] z-[9999] shadow-xl">
    <button 
        onClick={onClose}
        className="absolute top-4 right-4 text-white hover:text-white/70 transition-colors bg-transparent border-0 cursor-pointer"
    >
        <Icon name="close" size={24} />
    </button>

    <div className="mb-6">
        <h2 className="text-white text-2xl font-bold mb-2">Register</h2>
        <p className="text-white/80 text-sm">
        Already have an account? <button className="text-[#fad318] hover:underline bg-transparent border-0 cursor-pointer">Sign in</button>
        </p>
    </div>

    <div className="space-y-3 mb-6">
        <button 
        onClick={handleGoogleLogin}
        type="button"
        className="w-full bg-white text-black py-3 px-4 flex items-center justify-center gap-3 cursor-pointer border-0 transition-all duration-200 hover:bg-gray-100 font-semibold"
        >
        <svg width="20" height="20" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
        </svg>
        Continue with Google
        </button>

    </div>


    <div className="relative mb-6">
        <div className="absolute inset-0 flex items-center">
        <div className="w-full border-t border-white/20"></div>
        </div>
        <div className="relative flex justify-center text-sm">
        <span className="px-2 bg-[#4618ac] text-white/60">or</span>
        </div>
    </div>

    <form onSubmit={handleSubmit} className="space-y-4">
        <input
        type="email"
        placeholder="EXAMPLE@GMAIL.COM"
        value={email}
        onChange={(e) => setEmail(e.target.value)}
        className="w-full bg-[#32117d] border border-white/30 text-white px-4 py-3 focus:outline-none focus:border-[#00ff9d] placeholder:text-white/50"
        />
        <input
        type="text"
        placeholder="First Name"
        value={firstName}
        onChange={(e) => setFirstName(e.target.value)}
        className="w-full bg-[#32117d] border border-white/30 text-white px-4 py-3 focus:outline-none focus:border-[#00ff9d] placeholder:text-white/50"
        />
        <input
        type="text"
        placeholder="Last Name"
        value={lastName}
        onChange={(e) => setLastName(e.target.value)}
        className="w-full bg-[#32117d] border border-white/30 text-white px-4 py-3 focus:outline-none focus:border-[#00ff9d] placeholder:text-white/50"
        />
        <select
        value={role}
        onChange={(e) => setRole(e.target.value)}
        className="w-full bg-[#32117d] border border-white/30 text-white px-4 py-3 focus:outline-none focus:border-[#00ff9d] placeholder:text-white/50"
        >
        <option value="1">Buyer</option>
        <option value="2">Seller</option>
        </select>
        <div className="relative">
        <input
            type={showPassword ? "text" : "password"}
            placeholder="Password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            className="w-full bg-[#32117d] border border-white/30 text-white px-4 py-3 focus:outline-none focus:border-[#00ff9d] placeholder:text-white/50"
        />
        </div>

        <label className="flex items-center gap-2 cursor-pointer">
        <input
            type="checkbox"
            checked={showPassword}
            onChange={(e) => setShowPassword(e.target.checked)}
            className="w-4 h-4 cursor-pointer"
        />
        <span className="text-white/80 text-sm">Show password</span>
        </label>

        {error && (
        <div className="bg-red-500/20 border border-red-500 text-red-200 px-4 py-2 text-sm">
            {error}
        </div>
        )}

        <button
        type="submit"
        disabled={loading}
        className="w-full bg-[#fad318] text-black py-3 px-4 font-bold cursor-pointer border-0 transition-all duration-200 hover:bg-[#e5c115] disabled:opacity-50 disabled:cursor-not-allowed"
        >
        {loading ? 'Registering...' : 'Register'}
        </button>

        <div className="text-center">
        <button type="button" className="text-[#fad318] hover:underline bg-transparent border-0 cursor-pointer text-sm">
            Forgot password?
        </button>
        </div>

    </form>
    </div>
</>
);
};

export default RegisterModal;
